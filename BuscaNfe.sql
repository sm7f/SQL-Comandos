\\* Consulta de Evento *//

SELECT ID, NRNFE, VALORTOTALNFE, DATAEMISSAONFE, NATUREZAOPNFE, MODELO
FROM NFE
WHERE NRNFE BETWEEN 318002 AND 318007;

SELECT IDNFE, DATAHORAEVENTO, DESCRICAOEVENTO
FROM EVENTO_NFE
WHERE NRNFE BETWEEN 318002 AND 318007;

